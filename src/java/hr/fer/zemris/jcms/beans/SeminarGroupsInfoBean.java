package hr.fer.zemris.jcms.beans;

public class SeminarGroupsInfoBean {
	private String jmbag;
	private String kind;
	private String groupName;
	private String mentorID;
	private String title;
	private String isvuCode;
	
	public SeminarGroupsInfoBean() {
	}

	public String getJmbag() {
		return jmbag;
	}

	public void setJmbag(String jmbag) {
		this.jmbag = jmbag;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public String getMentorID() {
		return mentorID;
	}

	public void setMentorID(String mentorID) {
		this.mentorID = mentorID;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getIsvuCode() {
		return isvuCode;
	}

	public void setIsvuCode(String isvuCode) {
		this.isvuCode = isvuCode;
	}
	
}
